    private void insertarBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertarBtActionPerformed
        String codigo = codigoTf.getText();
        String nombre = nombreTf.getText();
        String idLoc = idLoctf.getText();
        String idMan = idMantf.getText();        
        if(codigo.length() <= 0 || nombre.length() == 0 || idLoc.length() == 0 || idMan.length() == 0){
          this.mensajeLb.setText(" Por favor, rellena todos los campos");
        }          
        try {
            String resultado = "";
            ConexionBaseDatos c1 = new ConexionBaseDatos();
            resultado = c1.altas(Integer.parseInt(codigoTf.getText()), nombreTf.getText(), Integer.parseInt(idLoctf.getText()), Integer.parseInt(idMantf.getText()));
            this.mensajeLb.setText(resultado);
            this.mostrar();
        } catch (NumberFormatException e) {
            this.mensajeLb.setText(" Ha ocurrido un error " + e.getMessage());
        }
    }//GEN-LAST:event_insertarBtActionPerformed

