    // Método para mostrar datos de departamentos 
    private void mostrar() {

        // Obtiene el modelo de la tabla y limpia cualquier dato anterior
        DefaultTableModel dm = (DefaultTableModel) tabla.getModel();
        dm.getDataVector().removeAllElements();

        // Crear una instancia para conectar con la base de datos
        ConexionBaseDatos cx = new ConexionBaseDatos();

        // Solicitar los datos de departamentos y guardarlos en un ArrayList
        ArrayList <Departamento> departamentos = cx.mostrar();

        // Itera sobre la lista de departamentos
        for (int i = 0; i < departamentos.size(); i++) {
            // Guarda en el array los departamentos 
            String[] datosDepartamento = {
                Integer.toString(departamentos.get(i).getCodigo()),
                departamentos.get(i).getNombre(),
                Integer.toString(departamentos.get(i).getIdLocalizacion()),
                Integer.toString(departamentos.get(i).getIdManager())
            };

            // Obtiene nuevamente el modelo de la tabla 
            DefaultTableModel dmo = (DefaultTableModel) tabla.getModel();
            // Añade los datos del departamento como una nueva fila en la tabla
            dmo.addRow(datosDepartamento);
        }
    }

