    private void borrarBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarBtActionPerformed
        try {
            // Diálogo de confirmación para borrar
            int confirmacion = JOptionPane.showConfirmDialog(this,
                        "¿Estás seguro de que deseas eliminar el departamento" + this.codigoTf.getText() + "?",
                        "Confirmar eliminación",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
            if (confirmacion == JOptionPane.YES_OPTION) {
                String resultado = "";
                ConexionBaseDatos c1 = new ConexionBaseDatos();
                resultado = c1.borrar(Integer.parseInt(codigoTf.getText()));

                this.mensajeLb.setText(resultado);
                this.mostrar();
            } else {
                mensajeLb.setText("Operación cancelada.");
            }
        } catch (NumberFormatException e) {
            this.mensajeLb.setText("Por favor, introduce un codigo");
        }
    }//GEN-LAST:event_borrarBtActionPerformed

